    private void btEntraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntraActionPerformed
        // TODO add your handling code here:
        try {
            int tamLista = 2;

            if (txtUsuario.getText().isEmpty() || txtSenha.getText().isEmpty()) {
                MensagemUsuario.mensagemAviso("Informe um usu√°rio e senha!");
                return;
            }

            usuarioLogin = sqlLite.consultaLogin(txtUsuario.getText().trim());

            if (usuarioLogin == null) {
                MensagemUsuario.mensagemAlerta("Usu√°rio n√£o cadastrado!");
                return;
            }

            if (usuarioLogin.getListUser().size() >= tamLista) {

                if (cbTipoUser.getSelectedItem() == null) {
                    MensagemUsuario.mensagemAviso("Este usuario Tem mais de um tipo!");
                    selecionaUsuario();
                    return;
                }

                if (cbTipoUser.getSelectedIndex() < 0) {
                    MensagemUsuario.mensagemAviso("Selecione um tipo de para usuario!");
                    return;
                }

                comparaTiposUsuario();
                return;

            }
            compararUsuarioCadastrado(usuarioLogin);
        } catch (Exception e) {
            usuarioLogin = null;
            MensagemUsuario.mensagemErro(e.getMessage());
        }

    }//GEN-LAST:event_btEntraActionPerformed

