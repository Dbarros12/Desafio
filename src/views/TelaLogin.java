    private void comparaTiposUsuario() {
        switch (cbTipoUser.getSelectedItem().toString()) {
            case "Administrador":
                compararUsuarioCadastrado(userAdm);
                break;
            case "Gerente":
                compararUsuarioCadastrado(userGrt);
                break;
            case "Cliente":
                compararUsuarioCadastrado(userClt);
                break;
            case "Usuario":
                compararUsuarioCadastrado(userUsr);
                break;
            default:

        }
    }

    private void selecionaUsuario() {

        for (UsuarioLogin user : usuarioLogin.getListUser()) {

            switch (user.getTipoDeUsuario()) {
                case "Administrador":
                    userAdm = user;
                    break;
                case "Gerente":
                    userGrt = user;

                    break;
                case "Cliente":
                    userClt = user;
                    break;
                case "Usuario":
                    userUsr = user;
                    break;
                default:

            }
            cbTipoUser.addItem(user.getTipoDeUsuario());
        }
        cbTipoUser.setVisible(true);

    }

    private void compararUsuarioCadastrado(UsuarioLogin user) {

        boolean testeUsuario = comparaString(user.getNomeUsuario(), txtUsuario.getText());
        boolean testeSenha = comparaString(user.getSenha(), txtSenha.getText());

        if (!testeUsuario || !testeSenha) {
            MensagemUsuario.mensagemAlerta("Usuario ou senha invalidos!");
            return;
        }

        if (user.getTipoDeUsuario().trim().equalsIgnoreCase("Administrador") && user.getIDUsuario() == 1) {
            lbCadastra.setEnabled(true);
            usuarioAdmintrador = true;
            MensagemUsuario.mensagemAviso("Bem vindo! " + txtUsuario.getText()
                    + "\n Agora voc√™ pode cadastra um novo usu√°rio!");
            return;

        }

        if (user.getTipoDeUsuario().trim().equalsIgnoreCase("Administrador") && user.getIDUsuario() > 1) {

            if (lbImgDigital.getIcon() == null) {
                MensagemUsuario.mensagemAviso("Imagem obrigatoria!");
                return;
            }

            if (!this.resultadoComparacaoImagens(user)) {
                MensagemUsuario.mensagemAlerta("Imagem invalida para este usu√°rio!");
                return;

            }
            lbCadastra.setEnabled(true);
            usuarioAdmintrador = true;
            MensagemUsuario.mensagemAviso("Bem vindo! " + txtUsuario.getText()
                    + "\n Agora voc√™ pode cadastra um novo usu√°rio!");
            return;

        }

        if (lbImgDigital.getIcon() == null) {
            MensagemUsuario.mensagemAviso("Imagem obrigatoria!");
            return;
        }

        if (!this.resultadoComparacaoImagens(user)) {
            MensagemUsuario.mensagemAlerta("Imagem invalida para este usu√°rio!");
            return;

        }

        cadastroCliente = new TelaCliente(user.getTipoDeUsuario(), user.getNomeUsuario());
        cadastroCliente.setVisible(true);
        this.dispose();

    }

    private boolean comparaString(String string1, String string2) {
        if (string1.equals(string2)) {
            return true;
        }

        return false;
    }

    private boolean resultadoComparacaoImagens(UsuarioLogin user) {
        boolean comparacaoImagens = false;
        try {

            imagem = new Imagem();
            compara = new ComparacaoDasImagens();
            String caminhoDaImagem = user.getCaminhoDaImagem() + "/" + user.getNomeDaImagem();
            this.image2 = imagem.coverterParaBufferedImage(caminhoDaImagem);
            this.image1 = imagem.coverterParaBufferedImage(this.imagemByte);
            return comparacaoImagens = compara.comparaImagem(image1, image2);

        } catch (Exception e) {
            return comparacaoImagens;

        }

    }

